package Pack01;

public class User {
	int userNum;
	String membershipDate;
	String userName;
	String userID;
	String userPW;
	String phone;
	String email;
	String address;
	int PostNum;
	String brithday;
	
	public int getUserNum() { return userNum; }
	public void setUserNum(int userNum) { this.userNum = userNum; }
	public String getMembershipDate() { return membershipDate; }
	public void setMembershipDate(String membershipDate) { this.membershipDate = membershipDate; }
	public String getUserName() { return userName; }
	public void setUserName(String userName) { this.userName = userName; }
	public String getUserID() { return userID; }
	public void setUserID(String userID) { this.userID = userID; }
	public String getUserPW() { return userPW; }
	public void setUserPW(String userPW) { this.userPW = userPW; }
	public String getPhone() { return phone; }
	public void setPhone(String phone) { this.phone = phone; }
	public String getEmail() { return email; }
	public void setEmail(String email) { this.email = email; }
	public String getAddress() { return address; }
	public void setAddress(String address) { this.address = address; }
	public int getPostNum() { return PostNum; }
	public void setPostNum(int postNum) { PostNum = postNum; }
	public String getBrithday() { return brithday; }
	public void setBrithday(String brithday) { this.brithday = brithday; }
	
}